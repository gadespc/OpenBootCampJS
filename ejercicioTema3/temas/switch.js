let nota = 4;

switch (nota) {
    case 5: 
        console.log("Sobresaliente");
        break;
    case 4:     
        console.log("Aprobado");
        break;
    case 3:
        console.log("Suficiente");
        break;
    case 2:
        console.log("No has aprobado por poco");
        break;
    case 1:
        console.log("Deficiente");
        break;
    default:
        console.log("Muy deficiente");
        break;
}